def ensure_dir_for_file(path: str):
    d = os.path.dirname(path)
    if d:
        os.makedirs(d, exist_ok=True)


def clip_and_normalise(p: np.ndarray) -> np.ndarray:
    p = np.maximum(p, 1e-300)
    return p / p.sum()


def read_psi_from_matrix_csv(path: str, q: float, gamma: float) -> float:
    """
    Reads psi_values.csv in matrix format:
        gamma\\q,0,0.1,...
        0.0,...
        0.25,...
    """
    with open(path, newline="") as f:
        rows = list(csv.reader(f))

    q_values = [float(x) for x in rows[0][1:]]
    q_idx = None
    for j, qv in enumerate(q_values):
        if abs(qv - q) < 1e-10:
            q_idx = j
            break

    if q_idx is None:
        raise ValueError(f"q={q} not found in {path}")

    for row in rows[1:]:
        if not row:
            continue
        g = float(row[0])
        if abs(g - gamma) < 1e-10:
            return float(row[1 + q_idx])

    raise ValueError(f"gamma={gamma} not found in {path}")


def build_ou_ctmc_generator(J: int, eta: float, n_std: float = 3.0):
    """
    Birth-death approximation for:
        dU_t = -eta U_t dt + dB_t.
    """
    std_stat = 1.0 / math.sqrt(2.0 * eta)
    theta = np.linspace(-n_std * std_stat, n_std * std_stat, J)
    h = theta[1] - theta[0]

    G = np.zeros((J, J), dtype=float)

    for i, x in enumerate(theta):
        drift = -eta * x
        up = 0.5 / (h * h) + max(drift, 0.0) / h
        down = 0.5 / (h * h) + max(-drift, 0.0) / h

        if i == 0:
            G[i, i + 1] = up + down
        elif i == J - 1:
            G[i, i - 1] = up + down
        else:
            G[i, i + 1] = up
            G[i, i - 1] = down

    for i in range(J):
        G[i, i] = -G[i].sum()

    return theta, G


def stationary_dist(G: np.ndarray) -> np.ndarray:
    J = G.shape[0]
    A = G.T.copy()
    A[-1, :] = 1.0
    b = np.zeros(J)
    b[-1] = 1.0
    try:
        pi = np.linalg.solve(A, b)
    except np.linalg.LinAlgError:
        pi = np.ones(J) / J
    return clip_and_normalise(pi)


def state_arrival_rates(theta, q, gamma, sigma, varphi, psi):
    signal = gamma * sigma * q * theta
    lam_a = varphi + psi * np.exp(-signal)
    lam_b = varphi + psi * np.exp(+signal)
    return lam_a, lam_b


def true_arrival_rates(U, q, gamma, sigma, varphi, psi):
    signal = gamma * sigma * q * U
    lam_a = varphi + psi * math.exp(-signal)
    lam_b = varphi + psi * math.exp(+signal)
    return lam_a, lam_b


def simulate_one_path(case: Dict[str, Any], psi: float, Nt: int, T: float, J: int, seed: int):
    rng = np.random.default_rng(seed)

    q = float(case["q"])
    gamma = float(case["gamma"])
    eta = float(case["eta"])
    sigma = float(case["sigma"])
    varphi = float(case["varphi"])

    dt = T / Nt
    p = math.sqrt(max(1.0 - q * q, 0.0))

    t = np.linspace(0.0, T, Nt + 1)
    U = np.zeros(Nt + 1)

    # Start from stationary distribution of OU.
    U[0] = rng.normal(0.0, 1.0 / math.sqrt(2.0 * eta))

    # PI filter: discrete Kalman approximation using price increments.
    Uhat_PI = np.zeros(Nt + 1)
    P = 1.0 / (2.0 * eta)

    a = 1.0 - eta * dt
    Qvar = dt
    c = sigma * q * dt
    R = sigma**2 * p**2 * dt + 1e-12

    # ARR-full filter.
    theta, G = build_ou_ctmc_generator(J, eta)
    pi = stationary_dist(G)
    Uhat_ARR = np.zeros(Nt + 1)
    Uhat_ARR[0] = float(theta @ pi)

    lam_a_state, lam_b_state = state_arrival_rates(theta, q, gamma, sigma, varphi, psi)

    for n in range(Nt):
        # Price increment generated by current U_n.
        dS = sigma * q * U[n] * dt + sigma * p * math.sqrt(dt) * rng.normal()

        # PI prediction/update.
        m_pred = a * Uhat_PI[n]
        P_pred = a * a * P + Qvar
        K = P_pred * c / (c * c * P_pred + R)
        Uhat_PI[n + 1] = m_pred + K * (dS - c * m_pred)
        P = (1.0 - K * c) * P_pred

        # Full market-order arrivals generated from continuous U_n.
        lam_a_true, lam_b_true = true_arrival_rates(U[n], q, gamma, sigma, varphi, psi)
        dMa = rng.poisson(max(lam_a_true * dt, 0.0))
        dMb = rng.poisson(max(lam_b_true * dt, 0.0))

        # CTMC prediction.
        pi_pred = pi + dt * (pi @ G)
        pi_pred = clip_and_normalise(pi_pred)

        # Full-arrival Bayes update using both ask and bid market-order counts.
        logw = np.log(np.maximum(pi_pred, 1e-300))
        logw += -(lam_a_state + lam_b_state) * dt
        logw += dMa * np.log(np.maximum(lam_a_state * dt, 1e-300))
        logw += dMb * np.log(np.maximum(lam_b_state * dt, 1e-300))
        logw -= np.max(logw)

        pi = clip_and_normalise(np.exp(logw))
        Uhat_ARR[n + 1] = float(theta @ pi)

        # True OU update.
        U[n + 1] = U[n] - eta * U[n] * dt + math.sqrt(dt) * rng.normal()

    mse_pi = float(np.mean((U - Uhat_PI) ** 2))
    mse_arr = float(np.mean((U - Uhat_ARR) ** 2))

    return {
        "seed": seed,
        "t": t,
        "U": U,
        "Uhat_PI": Uhat_PI,
        "Uhat_ARR_full": Uhat_ARR,
        "MSE_PI": mse_pi,
        "MSE_ARR_full": mse_arr,
        "psi": psi,
    }


def score_path(panel_type: str, mse_pi: float, mse_arr: float) -> float:
    """
    Choose visually informative sample paths.

    pi_favoured:
        PI should have low absolute MSE and beat ARR-full.

    arr_favoured:
        ARR-full should have low absolute MSE and beat PI.

    balanced:
        The two MSEs should be close, and both should be reasonably small.
    """
    gap = mse_arr - mse_pi

    if panel_type == "pi_favoured":
        # Prefer small PI error and a clear PI advantage.
        return gap - 2.0 * mse_pi

    if panel_type == "arr_favoured":
        # Prefer small ARR-full error and a clear ARR-full advantage.
        return -gap - 2.0 * mse_arr

    if panel_type == "balanced":
        # Prefer similar errors, but avoid paths where both filters are bad.
        return -abs(gap) - 0.5 * (mse_pi + mse_arr)

    raise ValueError(panel_type)


def pick_path(case, psi, Nt, T, J, seed_start, n_seed_trials):
    best = None
    best_score = -1e100

    for seed in range(seed_start, seed_start + n_seed_trials):
        out = simulate_one_path(case, psi, Nt, T, J, seed)
        sc = score_path(case["panel_type"], out["MSE_PI"], out["MSE_ARR_full"])
        if sc > best_score:
            best_score = sc
            best = out

    return best


def plot_cases(chosen: List[Dict[str, Any]], out_png: str):
    ensure_dir_for_file(out_png)

    fig, axes = plt.subplots(3, 1, figsize=(12, 11), sharex=True)

    for idx, (ax, item) in enumerate(zip(axes, chosen)):
        case = item["case"]
        out = item["result"]
        t = out["t"]

        ax.plot(t, out["U"], label=r"True $U_t$", linewidth=2.0)
        ax.plot(t, out["Uhat_PI"], label=r"PI $\widehat U_t$", linewidth=1.7)
        ax.plot(t, out["Uhat_ARR_full"], label=r"ARR-full $\widehat U_t$", linewidth=1.7)

        q = case["q"]
        gamma = case["gamma"]
        eta = case["eta"]
        sigma = case["sigma"]

        ax.set_title(
            f"({chr(97+idx)}) {case['name']}: "
            f"$(q,\\gamma,\\eta,\\sigma)=({q:g},{gamma:g},{eta:g},{sigma:g})$  "
            f"MSE(PI)={out['MSE_PI']:.4f}, "
            f"MSE(ARR-full)={out['MSE_ARR_full']:.4f}",
            fontsize=12,
        )
        ax.set_ylabel("value")
        ax.grid(True, alpha=0.3)
        ax.legend(loc="upper left", fontsize=9)

    axes[-1].set_xlabel("t")
    plt.tight_layout()
    plt.savefig(out_png, dpi=220, bbox_inches="tight")
    plt.close(fig)


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--psi_csv", type=str, default="outputs_full_arrival/43b_filter_mse_gamma10_J7/psi_values.csv")
    parser.add_argument("--out_png", type=str, default="figures_full_arrival_final/fig48_arrfull_filter_sample_paths_baseline.png")
    parser.add_argument("--out_json", type=str, default="figures_full_arrival_final/fig48_arrfull_filter_sample_paths_baseline.json")
    parser.add_argument("--Nt", type=int, default=1000)
    parser.add_argument("--T", type=float, default=1.0)
    parser.add_argument("--J", type=int, default=7)
    parser.add_argument("--seed_start", type=int, default=20260500)
    parser.add_argument("--n_seed_trials", type=int, default=200)
    args = parser.parse_args()

    chosen = []
    summary = []

    for case in CASES:
        psi = read_psi_from_matrix_csv(args.psi_csv, case["q"], case["gamma"])
        out = pick_path(
            case=case,
            psi=psi,
            Nt=args.Nt,
            T=args.T,
            J=args.J,
            seed_start=args.seed_start,
            n_seed_trials=args.n_seed_trials,
        )
        chosen.append({"case": case, "result": out})

        summary.append({
            "name": case["name"],
            "panel_type": case["panel_type"],
            "q": case["q"],
            "gamma": case["gamma"],
            "eta": case["eta"],
            "sigma": case["sigma"],
            "varphi": case["varphi"],
            "psi": psi,
            "chosen_seed": out["seed"],
            "MSE_PI": out["MSE_PI"],
            "MSE_ARR_full": out["MSE_ARR_full"],
            "MSE_gap_arr_minus_pi": out["MSE_ARR_full"] - out["MSE_PI"],
        })

    plot_cases(chosen, args.out_png)

    ensure_dir_for_file(args.out_json)
    with open(args.out_json, "w") as f:
        json.dump({
            "description": "Baseline ARR-full filter sample paths.",
            "Nt": args.Nt,
            "T": args.T,
            "J": args.J,
            "psi_csv": args.psi_csv,
            "seed_start": args.seed_start,
            "n_seed_trials": args.n_seed_trials,
            "cases": summary,
            "out_png": args.out_png,
        }, f, indent=2)

    print("Saved figure:", args.out_png)
    print("Saved summary:", args.out_json)
    print(json.dumps(summary, indent=2))


if __name__ == "__main__":
    main()
